public class Exer1Semana5{
    public void imprimirFatorial(){
        //Fatorial 20
        double a = 1;
        for(int f=1;f<=20;f++){
            a = a * f;

        }
        System.out.println(a);

        //Fatorial 30
        double e = 1;
        for(int z=1;z<=30;z++){
            e = e * z;
        }
        System.out.println(e);
        
        //Fatorial 40
        double c = 1;
        for(int d=1;d<=40;d++){
            c = c * d;
        }
        System.out.println(c);
    }
}